def warehouse_to_cluster(warehouse_name: str) -> str:
    """Map Ozon warehouse name to FBO cluster. Returns 'UNKNOWN' if no match."""
    if not warehouse_name:
        return "UNKNOWN"
    exact = _EXACT.get(warehouse_name)
    if exact:
        return exact
    lw = warehouse_name.lower()
    for kw, cl in _KEYWORDS:
        if kw in lw:
            return cl
    return "UNKNOWN"
